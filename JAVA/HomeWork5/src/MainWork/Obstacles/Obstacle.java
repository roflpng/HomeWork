package MainWork.Obstacles;

public class Obstacle {

    private int height;
    private int leight;
    public void maxHeight(int h){
        this.height = h;
    }
    public void maxLeight(int l){
        this.leight = l;
    }

    public int getHeight() {
        return height;
    }

    public int getLeight() {
        return leight;
    }
}
